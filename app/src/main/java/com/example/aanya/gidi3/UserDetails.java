package com.example.aanya.gidi3;



public class UserDetails {
    public String password;
    public String email;
    public String age;
    public String weight;

    public UserDetails (){

    }

    public UserDetails(String password, String email, String age, String weight) {
        this.password = password;
        this.email = email;
        this.age = age;
        this.weight = weight;
    }
}
